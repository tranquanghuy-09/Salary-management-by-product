    private void btnXoaChamCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaChamCongActionPerformed
        // TODO add your handling code here:
        int row = tblDanhSachChamCong.getSelectedRow();
        if (row != -1) {
            String maChamCong = tblDanhSachChamCong.getValueAt(row, 0).toString();
            String maPhanCong = tblDanhSachChamCong.getValueAt(row, 2).toString();

            if (chamCongCNDao.xoaChamCong(maChamCong, maPhanCong)) {
                JOptionPane.showMessageDialog(this, "Xóa chấm công thành công");
                try {
                    loadTableData2();
                } catch (Exception ex) {
                    Logger.getLogger(FrmChamCongCongNhan.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Xóa chấm công không thành công");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn phân đoạn để xóa");
        }

    }//GEN-LAST:event_btnXoaChamCongActionPerformed

