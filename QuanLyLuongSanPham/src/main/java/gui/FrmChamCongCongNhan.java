    private void btnChamCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChamCongActionPerformed
        // TODO add your handling code here:
        String maChamCong = txtMaChamCong.getText();
        String maPhanCong = txtMaPhanCong.getText();
        int soLuongSanPham = Integer.parseInt(txtSoLuongSanPham.getText());
        float heSoLuongCa = Float.parseFloat(txtHeSoCa.getText());
        String caLam = (String) jComboBoxCaLam.getSelectedItem();
        String trangThai = (String) jComboBoxTrangThai.getSelectedItem();
        String ngayChamCong = txtNgayChamCong.getText();
        PhanCong phanCong = new PhanCong();
        phanCong.setMaPC(maPhanCong);
        ChamCongCN chamCong = new ChamCongCN(maChamCong, phanCong, soLuongSanPham, heSoLuongCa, caLam, trangThai);
        ChamCongCNDao chamCongCNDao = new ChamCongCNDao();
        boolean result = chamCongCNDao.themChamCongCongNhan(chamCong);
        if (result) {
            JOptionPane.showMessageDialog(this, "Thêm chấm công thành công");
            try {
                loadTableData2();
            } catch (Exception ex) {
                Logger.getLogger(FrmChamCongCongNhan.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Thêm chấm công không thành công");
        }
    }//GEN-LAST:event_btnChamCongActionPerformed

