    private void loadTableData() {
        PhanCongCNDao phanCongDao = new PhanCongCNDao();
        List<PhanCong> phanCongs = phanCongDao.getAllPhanCong();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã phân công");
        model.addColumn("Mã phân đoạn");
        model.addColumn("Mã công nhân");
        model.addColumn("Số lượng cần làm");
        model.addColumn("Ngày phân công");
        model.addColumn("Ngày hoàn thành");
        model.addColumn("Trạng thái");

        for (PhanCong phanCong : phanCongs) {
            model.addRow(new Object[]{
                phanCong.getMaPC(),
                phanCong.getCongDoan().getMaPhanDoan(),
                phanCong.getCongNhan().getMaCongNhan(),
                phanCong.getSoLuongCanLam(),
                phanCong.getNgayPhanCong(),
                phanCong.getNgayHoanThanh(),
                phanCong.getTrangThai()
            });
        }
        tblDanhSachPhanCong.setModel(model);

    }

    private void loadTableData2() throws Exception {
        ChamCongCNDao chamCongCNDao = new ChamCongCNDao();
        List<ChamCongCN> chamCongList = chamCongCNDao.getAllChamCong();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã chấm công");
        model.addColumn("Mã công nhân");
        model.addColumn("Mã phân công");
        model.addColumn("Mã phân đoạn");
        model.addColumn("Số lượng cần làm");
        model.addColumn("Số lượng đã làm");
        model.addColumn("Ngày phân công");
        model.addColumn("Ca làm");
        model.addColumn("Hệ số lương ca");
        model.addColumn("Ngày chấm công");
        model.addColumn("Trạng thái");
        for (ChamCongCN chamCong : chamCongList) {
            model.addRow(new Object[]{
                chamCong.getMaChamCong(),
                chamCong.getPhanCong().getCongNhan().getMaCongNhan(),
                chamCong.getPhanCong().getMaPC(),
                chamCong.getPhanCong().getCongDoan().getMaPhanDoan(),
                chamCong.getPhanCong().getSoLuongCanLam(),
                chamCong.getSoLuongSanPham(),
                chamCong.getPhanCong().getNgayPhanCong(),
                chamCong.getCaLam(),
                chamCong.getHeSoLuongCa(),
                chamCong.getNgayChamCong(),
                chamCong.getTrangThai(),});
            tblDanhSachChamCong.setModel(model);
        }
    }

